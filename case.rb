my_weather = 'showers'

case my_weather
when 'snowing'
	puts "Better wrap up warm!"
when 'cloudy'
	puts "No need for the sunblock!"
when 'rainy'
	puts "Remember that umbrella!"
when 'foggy'
	puts "Wear bright clothing!"
when 'sunny'
	puts "Slip, slap, slop!"
else
	puts "Take a jacket just in case"
end